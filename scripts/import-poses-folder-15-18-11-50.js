async function importPoses() {
  console.log('Starting import of poses from folder 15-18 (011-050)...\n');

  let created = 0;
  let updated = 0;
  let errors = 0;

  for (const pose of poses) {
    // Check if pose already exists
    const { data: existing } = await supabase
      .from('poses')
      .select('id, slug')
      .eq('slug', pose.slug)
      .single();

    if (existing) {
      // Update existing pose
      const { error } = await supabase
        .from('poses')
        .update(pose)
        .eq('slug', pose.slug);

      if (error) {
        console.log(`Error updating ${pose.slug}:`, error.message);
        errors++;
      } else {
        console.log(`Updated: ${pose.english_name}`);
        updated++;
      }
    } else {
      // Insert new pose
      const { error } = await supabase
        .from('poses')
        .insert(pose);

      if (error) {
        console.log(`Error inserting ${pose.slug}:`, error.message);
        errors++;
      } else {
        console.log(`Created: ${pose.english_name}`);
        created++;
      }
    }
  }

  // Get final count
  const { count } = await supabase.from('poses').select('*', { count: 'exact', head: true });
  console.log(`\n=== Import Summary ===`);
  console.log(`Created: ${created}`);
  console.log(`Updated: ${updated}`);
  console.log(`Errors: ${errors}`);
  console.log(`Total poses in database: ${count}`);
}

importPoses();
