async function importPoses() {
  console.log("Importing 30 poses from folder 19-24...\n");

  let created = 0;
  let updated = 0;
  let errors = 0;

  for (const pose of poses) {
    // Check if pose already exists
    const { data: existing } = await supabase
      .from('poses')
      .select('slug')
      .eq('slug', pose.slug)
      .single();

    if (existing) {
      // Update existing pose
      const { error } = await supabase
        .from('poses')
        .update(pose)
        .eq('slug', pose.slug);

      if (error) {
        console.log(`Error updating ${pose.slug}: ${error.message}`);
        errors++;
      } else {
        console.log(`Updated: ${pose.english_name}`);
        updated++;
      }
    } else {
      // Create new pose
      const { error } = await supabase
        .from('poses')
        .insert(pose);

      if (error) {
        console.log(`Error creating ${pose.slug}: ${error.message}`);
        errors++;
      } else {
        console.log(`Created: ${pose.english_name}`);
        created++;
      }
    }
  }

  // Get final count
  const { count } = await supabase
    .from('poses')
    .select('*', { count: 'exact', head: true });

  console.log(`\n=== Import Complete ===`);
  console.log(`Created: ${created}`);
  console.log(`Updated: ${updated}`);
  console.log(`Errors: ${errors}`);
  console.log(`\nTotal poses in database: ${count}`);
}

importPoses();
