async function importPoses() {
  console.log('=== Importing 30 Poses (71-100) ===\n');

  let created = 0;
  let updated = 0;
  let errors = 0;

  for (const pose of poses) {
    // Check if pose exists
    const { data: existing } = await supabase
      .from('poses')
      .select('id, slug')
      .eq('slug', pose.slug)
      .single();

    if (existing) {
      // Update existing
      const { error } = await supabase
        .from('poses')
        .update(pose)
        .eq('slug', pose.slug);

      if (error) {
        console.log('ERROR updating ' + pose.slug + ': ' + error.message);
        errors++;
      } else {
        console.log('UPDATED: ' + pose.english_name);
        updated++;
      }
    } else {
      // Create new
      const { error } = await supabase
        .from('poses')
        .insert(pose);

      if (error) {
        console.log('ERROR creating ' + pose.slug + ': ' + error.message);
        errors++;
      } else {
        console.log('CREATED: ' + pose.english_name);
        created++;
      }
    }
  }

  console.log('\n=== Import Complete ===');
  console.log('Created: ' + created);
  console.log('Updated: ' + updated);
  console.log('Errors: ' + errors);

  // Get total count
  const { count } = await supabase.from('poses').select('*', { count: 'exact', head: true });
  console.log('\nTotal poses in database: ' + count);
}

